function add(num1, num2){
    return num1 + num2
}

function subtract(num1, num2){
    return num1 - num2
}

function multiply(num1, num2){
    return num1 * num2
}

function divide(num1, num2){
    return num1 / num2
}

console.log(add(6, 10))
console.log(subtract(6, 10))
console.log(multiply(6, 10))
console.log(divide(6, 10))